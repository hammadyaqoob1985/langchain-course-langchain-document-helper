async def main():
    """Main async function to orchestrate entire process."""
    log_header("DOCUMENT INGESTION PIPELINE")

    log_info(
        "TavilyCrawl: Starting to crawl documentation from https://python.langchain.com/",
        Colors.PURPLE
    )

    tavily_crawl_results = tavily_crawl.invoke(
        {
            "url": "https://python.langchain.com/",
            "max_depth": 3,
            "extract_depth": "advanced",
            "instructions": "Documentation relevant to ai agents"
        }
    )

    all_docs = [Document(page_content=result['raw_content'], metadata={"source": result['url']}) for result in tavily_crawl_results['results']]

    log_success(
        f"TavillyCrawl: Successfully crawled {len(all_docs)} URLs from documentation site"
    )

    # Split Documents into chunks
    log_header("DOCUMENT CHUNKING PHASE")

    log_info(
        F"Text Splitter: Processing {len(all_docs)} documents with 4000 chunk size and 200 overlap",
        Colors.YELLOW
    )
    text_splitter = RecursiveCharacterTextSplitter(chunk_size=4000, chunk_overlap=200)
    splitted_docs = text_splitter.split_documents(all_docs)
    log_info(
        F"Text Splitter: Created {len(splitted_docs)} chunks from {len(all_docs)} documents",
        Colors.PURPLE
    )

    # Process documents asynchronously
    await index_documents_async(splitted_docs, batch_size=500)

    log_header("PIPELINE COMPLETE")
    log_success("ðŸŽ‰ Documentation ingestion pipeline finished successfully!")
    log_info("ðŸ“Š Summary:", Colors.BOLD)
    log_info(f"   â€¢ Pages crawled: {len(tavily_crawl_results)}")
    log_info(f"   â€¢ Documents extracted: {len(all_docs)}")
    log_info(f"   â€¢ Chunks created: {len(splitted_docs)}")

if __name__ == "__main__":
    asyncio.run(main())